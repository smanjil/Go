
package main

import "fmt"

func main(){
    firstNumber := 60
    secondNumber := 5

    fmt.Println("Sum: ", firstNumber + secondNumber)
    fmt.Println("Subtraction: ", firstNumber - secondNumber)
    fmt.Println("Multiplication: ", firstNumber * secondNumber)
    fmt.Println("Division: ", firstNumber / secondNumber)
    fmt.Println("Modulus (Remainder): ", firstNumber % secondNumber)
}
